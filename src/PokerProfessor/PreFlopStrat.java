	public int getPreFlopScore(int []c){

		if(c[0] < c[1]){
			int temp = c[0];
			c[0] = c[1];
			c[1] = temp;
		}
		
		int highSuit = c[0]%10;
		int lowSuit = c[1]%10;

		c[0] = c[0] - (highSuit);
		c[1] = c[1] - (highSuit==lowSuit?lowSuit:lowSuit-1);

		
		for(int g=0;g<pF.length;g++){
			if(c[0] == pF[g][0] &&c[1] == pF[g][1])
				return 170-g;
		}
		
		System.out.println("in pre flop strat should never get here - could not find match for preflop cards : " + c[0] + ", " + c[1]);
		return -1;//should never come to this
	}
	
